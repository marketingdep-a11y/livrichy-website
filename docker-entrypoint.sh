    php artisan statamic:eloquent:import-entries

    # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –Ω–∞–≤–∏–≥–∞—Ü–∏–∏ –∏–∑ —Ñ–∞–π–ª–æ–≤ –≤ –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö
