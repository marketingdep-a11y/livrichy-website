#!/bin/bash
set -e

echo "üöÄ Starting Laravel/Statamic application initialization..."

# –ü—Ä–æ–µ–∫—Ç –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –ø—Ä—è–º–æ –≤ /app (Base Directory = /)
APP_DIR="/app"

